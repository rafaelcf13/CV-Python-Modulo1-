a=float(input('Informe a altura da parede em metros: '))
l=float(input('Informe a largura da parede em metros: '))
area=a*l
print('Sua parede tem {} de altura, {} de largura e area de {}m2'.format(a, l, area))
tinta=area / 2
print('Para pintar esta parede voce vai precisar de {} litros de tinta'.format(tinta))

